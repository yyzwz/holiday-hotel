<template>
<div class="search">
    <add v-if="currView == 'add'" @close="currView = 'index'" @submited="submited" />
    <edit v-if="currView == 'edit'" @close="currView = 'index'" @submited="submited" :data="formData" />
    <Card v-show="currView == 'index'">
        <Row v-show="openSearch" @keydown.enter.native="handleSearch">
            <Form ref="searchForm" :model="searchForm" inline :label-width="0">
                <Form-item ref="searchForm" :model="searchForm" inline :label-width="0" style="display: flex">
                    <Form-item label="" prop="newTitle">
                        <Input type="text" v-model="searchForm.newTitle" placeholder="请输入新闻标题" clearable style="width: 200px" @on-clear="onClear" />
                    </Form-item>
                    <Form-item style="margin-left: 10px" class="br">
                        <Button @click="handleSearch" type="primary" icon="ios-search" size="small" ghost>搜索</Button>
                        <Button @click="handleReset" type="warning" size="small" icon="md-refresh" ghost>重置</Button>
                        <Button @click="add" type="info" size="small" icon="md-add" ghost>添加</Button>
                        <Button @click="delAll" type="error" icon="md-trash" size="small" ghost>删除</Button>
                        <Button @click="excelData" type="success" icon="md-paper-plane" size="small" ghost>导出</Button>
                    </Form-item>
                    <Form-item style="position: fixed; right: 50px; top: 130px">
                        <Button type="info" @click="showFilterPanelFlag = !showFilterPanelFlag" class="showFilterPanelFlag" icon="md-settings" size="small">
                        </Button>
                        <Button type="warning" @click="modal1 = true" class="showFilterPanelFlag" icon="ios-help-circle-outline" size="small">
                        </Button>
                        <Modal v-model="modal1" title="使用教程">
                            <p>使用教程</p>
                        </Modal>
                    </Form-item>
                </Form-item>
            </Form>
        </Row>
        <Row class="operation" style="position: relative">
            <transition>
                <div v-show="showFilterPanelFlag" class="filter-panel">
                    <CheckboxGroup v-model="selected">
                        <div v-for="item in mycolumns" :key="item.key">
                            <Checkbox :label="item.title" style="margin: 2px 5px"></Checkbox>
                        </div>
                    </CheckboxGroup>
                </div>
            </transition>
        </Row>
        <Row v-show="openTip"> </Row>
        <Row>
            <Table :loading="loading" border :columns="columns" :data="data" ref="table" sortable="custom" @on-sort-change="changeSort" @on-selection-change="changeSelect" @on-row-click="rowClick" :row-class-name="rowClassNmae"></Table>
        </Row>
        <Row type="flex" justify="end" class="page">
            <Page :current="searchForm.pageNumber" :total="total" :page-size="searchForm.pageSize" @on-change="changePage" @on-page-size-change="changePageSize" :page-size-opts="[5, 10, 50]" size="small" show-total show-elevator show-sizer></Page>
        </Row>
    </Card>
</div>
</template>

<script>
import {
    getDynamic_newList,
    deleteDynamic_new,
    setTopById,
    setPublicById,
} from "./api.js";
import add from "./add.vue";
import edit from "./edit.vue";
import Viewer from "viewerjs";
export default {
    name: "single-window",
    components: {
        add,
        edit,
    },
    data() {
        return {
            selected: [
                "选择",
                "序号",
                "新闻标题",
                "新闻描述",
                "图片",
                "新闻时效",
                "关联服务",
                "发布机构",
                "是否公开",
                "是否置顶",
                "操作",
            ],
            modal1: false,
            openSearch: true,
            openTip: true,
            formData: {},
            currView: "index",
            loading: true, // 表单加载状态
            searchForm: {
                // 搜索框初始化对象
                newTitle: "",
                pageNumber: 1,
                pageSize: 5, // 页面大小
                sort: "createTime", // 默认排序字段
                order: "desc", // 默认排序方式
            },
            selectList: [],
            selectCount: 0,
            selectRow: 0,
            columns: [{
                    type: "selection",
                    width: 60,
                    title: "选择",
                    align: "center",
                    fixed: "left",
                },
                {
                    title: "序号",
                    width: 85,
                    align: "center",
                    fixed: "left",
                    sortType: true,
                    render: (h, params) => {
                        return h(
                            "span",
                            params.index +
                            (this.searchForm.pageNumber - 1) * this.searchForm.pageSize +
                            1
                        );
                    },
                },
                {
                    title: "新闻标题",
                    key: "newTitle",
                    minWidth: 120,
                    sortable: false,
                    fixed: "left",
                },
                {
                    title: "新闻描述",
                    key: "newDescribe",
                    minWidth: 160,
                    sortable: false,
                    tooltip: true,
                    render: (h, params) => {
                        return h('pre', {
                            domProps: {
                                innerHTML: params.row.newDescribe
                            }
                        })
                    }
                },
                {
                    title: "图片",
                    key: "photo",
                    minWidth: 100,
                    sortable: false,
                    render: (h, params) => {
                        if (params.row.photo != undefined) {
                            return h("img", {
                                attrs: {
                                    src: params.row.photo,
                                },
                                style: {
                                    cursor: "zoom-in",
                                    width: "100px",
                                    margin: "10px 0",
                                    "object-fit": "contain",
                                },
                                on: {
                                    click: () => {
                                        this.showPic(params.row.photo);
                                    },
                                },
                            });
                        } else {
                            return h("div", [
                                h(
                                    "span", {
                                        style: {
                                            color: "#ff9900",
                                        },
                                    },
                                    "未上传"
                                ),
                            ]);
                        }
                    }
                },
                {
                    title: "新闻时效",
                    key: "periodTime",
                    minWidth: 120,
                    sortable: false,
                },
                {
                    title: "是否置顶",
                    key: "isTop",
                    minWidth: 120,
                    sortable: false,
                    render: (h, params) => {

                        return h("i-switch", {
                            props: {
                                size: "large",
                                value: params.row.isTop === "yes",
                            },
                            scopedSlots: {
                                open: () => h("span", "开启"),
                                close: () => h("span", "关闭"),
                            },
                            on: {
                                "on-change": (value) => {
                                    setTopById({
                                        id: params.row.id
                                    }).then((res) => {
                                        if (res.success) {
                                            this.$Message.success("操作成功");
                                            this.getDataList();
                                        }
                                    });
                                },
                            },
                        });
                        // 无需编辑的代码
                        // let re = "已置顶",
                        //     color = "";
                        // if (params.row.isTop == "yes") color = "blue";
                        // else {
                        //     color = "red";
                        //     re = "非置顶";
                        // }
                        // return h("div", [
                        //     h(
                        //         "Tag", {
                        //             props: {
                        //                 color: color,
                        //             },
                        //         },
                        //         re
                        //     ),
                        // ]);
                    },
                },
                {
                    title: "是否公开",
                    key: "isPublic",
                    minWidth: 120,
                    sortable: false,
                    render: (h, params) => {
                        return h("i-switch", {
                            props: {
                                size: "large",
                                value: params.row.isPublic === "yes",

                            },
                            scopedSlots: {
                                open: () => h("span", "开启"),
                                close: () => h("span", "关闭"),
                            },
                            on: {
                                "on-change": (value) => {
                                    setPublicById({
                                        id: params.row.id
                                    }).then((res) => {
                                        if (res.success) {
                                            this.$Message.success("操作成功");
                                            this.getDataList();
                                        }
                                    });
                                },
                            },
                        });
                    },
                },
                {
                    title: "创建时间",
                    key: "createTime",
                    sortable: true,
                    sortType: "desc",
                    minWidth: 180,
                    align: "center",
                    tooltip: true,
                },
                {
                    title: "创建者",
                    key: "createBy",
                    sortable: true,
                    sortType: "desc",
                    minWidth: 100,
                    align: "center",
                    tooltip: true,
                },
                {
                    title: "修改时间",
                    key: "updateTime",
                    sortable: true,
                    sortType: "desc",
                    minWidth: 180,
                    align: "center",
                    tooltip: true,
                },
                {
                    title: "修改者",
                    key: "updateBy",
                    sortable: true,
                    sortType: "desc",
                    minWidth: 100,
                    align: "center",
                    tooltip: true,
                },
                {
                    title: "操作",
                    key: "action",
                    align: "center",
                    fixed: "right",
                    width: 200,
                    render: (h, params) => {
                        return h("div", [
                            h(
                                "Button", {
                                    props: {
                                        type: "primary",
                                        size: "small",
                                        ghost: true,
                                        icon: "ios-create-outline"
                                    },
                                    style: {
                                        marginRight: "5px",
                                    },
                                    on: {
                                        click: () => {
                                            this.edit(params.row);
                                        },
                                    },
                                },
                                "编辑"
                            ),
                            h(
                                "Button", {
                                    props: {
                                        type: "error",
                                        size: "small",
                                        ghost: true,
                                        icon: "md-trash"
                                    },
                                    on: {
                                        click: () => {
                                            this.remove(params.row);
                                        },
                                    },
                                },
                                "删除"
                            ),
                        ]);
                    },
                },
            ],
            data: [],
            pageNumber: 1,
            pageSize: 5, // 页面大小
            total: 0,
            showFilterPanelFlag: false,
        };
    },
    methods: {
        init() {
            this.getDataList();
        },
        showPic(v) {
            let image = new Image();
            image.src = v;
            let viewer = new Viewer(image, {
                hidden: function () {
                    viewer.destroy();
                },
            });
            viewer.show();
        },
        onClear() {
            this.searchForm.newTitle = "";
            this.getDataList();
        },
        submited() {
            this.currView = "index";
            this.getDataList();
        },
        changePage(v) {
            this.searchForm.pageNumber = v;
            this.getDataList();
            this.clearSelectAll();
        },
        changePageSize(v) {
            this.searchForm.pageSize = v;
            this.getDataList();
        },
        rowClick(row, index) {
            this.selectRow = row;
        },
        rowClassNmae(row, index) {
            if (row.id == this.selectRow.id) {
                return "rowClassNmaeColor";
            }
            return "";
        },
        excelData() {
            this.$refs.table.exportCsv({
                filename: "导出结果",
            });
        },
        handleSearch() {
            this.searchForm.pageNumber = 1;
            this.searchForm.pageSize = 5;
            this.getDataList();
        },
        handleReset() {
            this.$refs.searchForm.resetFields();
            this.searchForm.pageNumber = 1;
            this.searchForm.pageSize = 5;
            this.getDataList();
        },
        changeSort(e) {
            this.searchForm.sort = e.key;
            this.searchForm.order = e.order;
            if (e.order === "normal") {
                this.searchForm.order = "";
            }
            this.getDataList();
        },
        clearSelectAll() {
            this.$refs.table.selectAll(false);
        },
        changeSelect(e) {
            this.selectList = e;
            this.selectCount = e.length;
        },
        getDataList() {
            this.loading = true;
            getDynamic_newList(this.searchForm).then((res) => {
                this.loading = false;
                if (res.success) {
                    this.data = res.result.records;
                    this.total = res.result.total;
                    for (var i = 0; i < res.result.records.length; i++) {
                        if (
                            !(
                                JSON.parse(this.getStore("userInfo")).departmentId ==
                                res.result.records[i].nursingOrganizationId ||
                                JSON.parse(this.getStore("userInfo")).organizationId ==
                                res.result.records[i].nursingOrganizationId
                            )
                        )
                            res.result.records[i]._disabled = true;
                    }
                }
            });
        },
        add() {
            this.currView = "add";
        },
        edit(v) {
            for (let attr in v) {
                if (v[attr] == null) {
                    v[attr] = "";
                }
            }
            let str = JSON.stringify(v);
            let data = JSON.parse(str);
            this.formData = data;
            this.currView = "edit";
        },
        remove(v) {
            this.$Modal.confirm({
                title: "确认删除",
                content: "您确认要删除 " + v.newTitle + " ?",
                loading: true,
                onOk: () => {
                    deleteDynamic_new({
                        ids: v.id
                    }).then((res) => {
                        this.$Modal.remove();
                        if (res.success) {
                            this.$Message.success("操作成功");
                            this.getDataList();
                        }
                    });
                },
            });
        },
        delAll() {
            if (this.selectCount <= 0) {
                this.$Message.warning("您还未选择要删除的数据");
                return;
            }
            this.$Modal.confirm({
                title: "确认删除",
                content: "您确认要删除所选的 " + this.selectCount + " 条数据?",
                loading: true,
                onOk: () => {
                    let ids = "";
                    this.selectList.forEach(function (e) {
                        ids += e.id + ",";
                    });
                    ids = ids.substring(0, ids.length - 1);
                    deleteDynamic_new({
                        ids: ids
                    }).then((res) => {
                        this.$Modal.remove();
                        if (res.success) {
                            this.$Message.success("操作成功");
                            this.clearSelectAll();
                            this.getDataList();
                        }
                    });
                },
            });
        },
    },
    mounted() {
        this.init();
        this.mycolumns = this.columns;
        let showcolumns = [];
        for (var i = 0; i < this.selected.length; i++) {
            var item = this.selected[i];
            for (var j = 0; j < this.columns.length; j++) {
                if (this.columns[j].title == item) {
                    showcolumns.push(this.columns[j]);
                }
            }
        }
        this.columns = showcolumns;
    },
    watch: {
        selected: function (newcolumns) {
            let showcolumns = [];
            for (var i = 0; i < this.mycolumns.length; i++) {
                var item = this.mycolumns[i];
                if (item.title == undefined) showcolumns.push(item);
                else if (newcolumns.contains(item.title)) showcolumns.push(item);
            }
            this.columns = showcolumns;
        },
    },
};
</script>

